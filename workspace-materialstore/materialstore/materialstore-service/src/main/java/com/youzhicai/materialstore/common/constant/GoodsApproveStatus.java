package com.youzhicai.materialstore.common.constant;

/**
 * @ClassName: GoodsApproveStatus
 * @Description: TODO(这里用一句话描述这个类的作用)
 * @author XieXianpeng
 * @date 2018年10月16日 下午6:16:06
 */
public enum GoodsApproveStatus {
    /** 待提交 */
    UNSUBMIT(11, "待提交"),

    /** 审核中 */
    SUBMITTED(21, "审核中"),

    /** 不通过 */
    UNPASS(31, "不通过"),

    /** 已通过 */
    PASSED(1, "已通过"),;

    private int code;

    private String msg;

    private GoodsApproveStatus(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }

}
